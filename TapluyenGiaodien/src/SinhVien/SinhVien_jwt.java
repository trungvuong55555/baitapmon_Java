    private void jButtonThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonThemActionPerformed
        // TODO add your handling code here:
        this.masv= jTextFieldMasv.getText().trim();
        this.hoten= jTextFieldHoten.getText().trim();
        if(jRadioButtonNam.isSelected())
        {
            this.gioitinh="Nam";
        }
        else
            this.gioitinh="Nu";
        
        this.lop= jComboBoxLop.getItemAt(jComboBoxLop.getSelectedIndex());
        
        this.sothich="";
        if(jCheckBoxLaptrinh.isSelected())
            this.sothich= jCheckBoxLaptrinh.getText();
        if(jCheckBoxGame.isSelected())
            this.sothich= jCheckBoxGame.getText();
        if(jCheckBoxKhac.isSelected())
            this.sothich= jCheckBoxKhac.getText();
        
        SinhVien a= new SinhVien(this.masv,this.hoten,this.gioitinh,this.lop,this.sothich);
        list.add(a);
        LoadModel();
    }//GEN-LAST:event_jButtonThemActionPerformed

