    private void jTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMouseClicked
        // TODO add your handling code here:
        this.row= jTable.getSelectedRow();
        jTextFieldMasv.setText(list.get(this.row).getMasv());
        jTextFieldHoten.setText(list.get(this.row).getHoten());
        
        if(list.get(this.row).getGioitinh().equals("Nam"))
        {
            jRadioButtonNam.setSelected(true);
        }
        else
            jRadioButtonNu.setSelected(true);
        
        if(list.get(this.row).getLop().equals("Khoa hoc may tinh 1"))
        {
            jComboBoxLop.setSelectedIndex(0);
        }
        else
           jComboBoxLop.setSelectedIndex(1);
        
        if(list.get(row).getSothich().equals("Game"))
            jCheckBoxGame.setSelected(true);
        else if(list.get(row).getSothich().equals("Lap trinh"))
            jCheckBoxLaptrinh.setSelected(true);
        else if(list.get(row).getSothich().equals("Khac"))
            jCheckBoxKhac.setSelected(true);
        else
        {
            jCheckBoxGame.setSelected(false);
            jCheckBoxLaptrinh.setSelected(false);
            jCheckBoxKhac.setSelected(false);
        }
        
        
    }//GEN-LAST:event_jTableMouseClicked

